int add(int a,int b);
int sub(int a,int b);
int mul(int a,int b);
int div(int a,int b);

int add(int a,int b){
	int result = a+b;
	return result;
}

int sub(int a,int b){
	int result = a-b;
	return result;
}

int mul(int a,int b){
	int result = a*b;
	return result;
}

int div(int a,int b){
	int result = a/b;
	return result;
}

